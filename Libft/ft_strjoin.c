#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	size_t	maxlen;
	size_t	i;
	size_t	j;
	char	*str;

	maxlen = ft_strlen(s1) + ft_strlen(s2) + 1;
	str = malloc(maxlen);
	if (str == NULL)
		return (NULL);
	i = 0;
	j = 0;
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	while (s2[j])
	{
		str[i] = s2[j];
		i++;
		j++;
	}
	str[i] = '\0';
	return (str);
}
